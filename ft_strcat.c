#include "libft.h"

char		*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int k;

	i = 0;
	k = ft_strlen(s1);
	while (s2[i])
	{
		s1[k] = s2[i];
		k++;
		i++;
	}
	s1[k] = '\0';
	return (s1);
}
